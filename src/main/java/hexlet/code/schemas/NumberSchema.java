package hexlet.code.schemas;

/**
 * Схема для проверки числовых значений.
 *
 * <p>Предоставляет гибкие возможности настройки ограничения:
 *  - можно требовать только положительные числа;
 *  - задать диапазон допустимых значений (минимум и максимум).</p>
 */
public class NumberSchema extends BaseSchema<NumberSchema> {

    /** Флаг, указывающий, что число должно быть строго положительным. */
    private boolean positive = false;

    /** Минимальное значение в заданном диапазоне; {@code null} означает отсутствие ограничения. */
    private Integer minRange = null;

    /** Максимальное значение в заданном диапазоне; {@code null} означает отсутствие ограничения. */
    private Integer maxRange = null;

    /**
     * Устанавливает требование, что валидное число должно быть положительным.
     *
     * @return ссылка на текущий объект для поддержки метод‑чaining
     */
    public NumberSchema positive() {
        positive = true;
        return this;
    }

    /**
     * Ограничивает допустимый диапазон чисел от {@code min} до {@code max}.
     *
     * @param min минимальное значение (включительно)
     * @param max максимальное значение (включительно)
     * @return ссылка на текущий объект для поддержки метод‑чaining
     */
    public NumberSchema range(int min, int max) {
        minRange = min;
        maxRange = max;
        return this;
    }

    /**
     * Проверяет, удовлетворяет ли переданное значение условиям схемы.
     *
     * <p>Алгоритм проверки:</p>
     * <ol>
     *   <li>Если {@code value} равно {@code null}, то валидность определяется
     *       только наличием обязательного требования ({@link BaseSchema#required}).</li>
     *   <li>Преобразуем значение в {@link Number}. Это может вызвать {@link ClassCastException}
     *       если тип объекта не совместим с {@code Number}.</li>
     *   <li>Если выставлен флаг {@code positive}, проверяем, что число > 0.
     *       Обратите внимание на использование {@code doubleValue()} для сравнения,
     *       поскольку это работает как с целыми, так и со дробными числами.</li>
     *   <li>Проверяем границы диапазона:
     *       - если {@code minRange} задано (не {@code null}), то число должно быть ≥ {@code minRange};
     *       - аналогично для {@code maxRange}. Эти проверки также используют {@code doubleValue()}
     *         для универсальности.</li>
     * </ol>
     *
     * @param value объект, который нужно проверить
     * @return {@code true} если значение удовлетворяет всем ограничениям схемы,
     *         {@code false} иначе
     */
    @Override
    public boolean isValid(Object value) {
        // Проверяем наличие значения.
        if (value == null) {
            return !required; // Если поле не обязательное, то отсутствие значения считается валидным.
        }

        // Преобразуем значение в Number. Здесь потенциально сложное место:
        // если переданный объект не реализует интерфейс {@code Number},
        // будет выброшено исключение ClassCastException.
        Number num = (Number) value;

        // Проверяем положительность, если требуется.
        if (positive && num.doubleValue() <= 0) {
            return false; // Негативное или нулевое число не удовлетворяет требованию.
        }

        // Проверяем нижнюю границу диапазона, если она задана.
        if (minRange != null && num.doubleValue() < minRange) {
            return false; // Число меньше минимального допустимого значения.
        }

        // Проверяем верхнюю границу диапазона, если она задана.
        if (maxRange != null && num.doubleValue() > maxRange) {
            return false; // Число больше максимального допустимого значения.
        }

        // Если все проверки прошли успешно – значение валидно.
        return true;
    }
}
